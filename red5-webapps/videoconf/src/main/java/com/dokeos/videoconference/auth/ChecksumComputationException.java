public class ChecksumComputationException extends Exception {

    public ChecksumComputationException() {
        super();
    }

    public ChecksumComputationException(String message, Throwable cause) {
        super(message, cause);
    }

    public ChecksumComputationException(String message) {
        super(message);
    }

    public ChecksumComputationException(Throwable cause) {
        super(cause);
    }
}
